public class LoopDemo1{
	public static void main(String[]  args){

		int counter = 1;
		while(counter <= 10){
			

		System.out.println("  "+counter);
			counter++;
		}
	

		
		for(int index = 1; index <= 10; index++){
				
			System.out.printf("%d ",index);
				
		}

		


}

}